const bestsellerBtn = document.querySelector('.bestseller-btn');
const popuupBox = document.querySelector('.popuup-box');

bestsellerBtn.addEventListener('click', () => {
  if (popuupBox.style.display === "none") {
    popuupBox.style.display = "block";
  } else {
    popuupBox.style.display = "none";
  }
});

